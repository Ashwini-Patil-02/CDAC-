//2. Write a program to compute the factorial of the number 10.
class Ques2{
          public static void main(String args[]){
		  int fact=1, i;
		  for(i=1; i<=10; i++)
		     {
		            fact= fact*i;
		  
		      }
			  
			  System.out.println("the factorial is:" + fact);
			  
		    }
        }
